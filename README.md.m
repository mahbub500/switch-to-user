# WordPress User Switcher Plugin

Effortlessly switch between WordPress user accounts with the User Switcher Plugin! Ideal for developers, administrators, and site managers, this plugin simplifies testing, troubleshooting, and managing user roles.

## Description

The **WordPress User Switcher Plugin** allows administrators and developers to quickly switch between different user accounts without logging out. This plugin is especially useful for troubleshooting, testing, and managing users, making it a must-have tool for anyone who frequently works with multiple WordPress user accounts.

- **Seamless User Switching**: Switch between any user account with just a few clicks.
- **User Role Management**: Ideal for testing user roles and permissions without logging out.
- **Easy to Use**: One-click switching directly from the WordPress admin bar.

## Features

- Quickly switch between different WordPress user accounts.
- Ideal for testing and troubleshooting with various user roles.
- Streamlines user role management for administrators and developers.
- Simple user interface with minimal setup required.
- Popup interface to search for users by name or email.

## Installation

1. Download the plugin ZIP file.
2. Log in to your WordPress admin dashboard.
3. Go to **Plugins** > **Add New**.
4. Click **Upload Plugin**, select the downloaded ZIP file, and click **Install Now**.
5. After installation, click **Activate** to enable the plugin.

## Usage

1. Navigate to the plugin\'s Admin bar and click **User Switcher**.
2. Click on the **Switch To User** button next to the desired user.
3. A popup will appear, allowing you to search for a user by name or email. After selecting the user, click **Go** to sign in as that user.
4. Instantly switch and manage different accounts without logging out.

